HEADERBLOCK:	ORG	0A000H
	MOV	R6, #50h
	MOV	R5, #00h
	MOV	R3, #00h
	MOV	A, #3Ah
	MOV	DPH, #0B0h
	MOV	DPL, R6
	MOVX	@DPTR, A
	INC	DPTR
	INC	R6
	MOV	A, #10h
	MOVX	@DPTR, A
	ADD	A, R3
	MOV	R3, A
	INC	DPTR
	INC	R6
	MOV	A, #0B0h
	MOVX	@DPTR, A
	ADD	A, R3
	MOV	R3, A
	INC	DPTR
	INC	R6
	MOV	A, #00h
	MOVX	@DPTR, A
	ADD	A, R3
	MOV	R3, A
	INC	DPTR
	INC	R6
	MOV	A, #00h
	MOVX	@DPTR, A
	ADD	A, R3
	MOV	R3, A
	INC	DPTR
	INC	R6
	MOV	R4, #10h

LOOP1:
	MOV	DPL, R5
	MOVX	A, @DPTR
	ADD	A, R3
	MOV	R3, A
	MOVX	A, @DPTR
	MOV	DPL, R6
	MOVX	@DPTR, A
	DEC	R4
	INC	R5
	INC	R6
	CJNE	R4, #00h, LOOP1

CHECKSUM:
	MOV	A, R3
	CPL	A
	INC	A
	MOV	DPL, R6
	MOVX	@DPTR, A
	END
